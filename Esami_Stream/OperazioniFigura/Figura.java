package Ingengeria_Del_SW.Figura.OperazioniFigura;

public class Figura {
    private int l1,  l2,  l3,  l4,  a1,  a2,  a3,  a4;

    public Figura(int l1, int l2, int l3, int l4, int a1, int a2, int a3, int a4){
        this.l1 = l1;
        this.l2 = l2;
        this.l3 = l3;
        this.l4 = l4;
        this.a1 = a1;
        this.a2 = a2;
        this.a3 = a3;
        this.a4 = a4;
    }

    public int l1(){
        return l1;
    }

    public int l2(){
        return l2;
    }

    public int l3(){
        return l3;
    }

    public int l4(){
        return l4;
    }

    public int a1(){
        return a1;
    }

    public int a2(){
        return a2;
    }

    public int a3(){
        return a3;
    }

    public int a4(){
        return a4;
    }


}
